def metade(preco):
    metade_preco = preco/2

    print(f'A metade de R${preco} é R$ {metade_preco}')

def dobro(preco):
    dobro_preco = preco * 2

    print(f'O dobro do R${preco} é R$ {dobro_preco}')

def aumento(preco):
    aumento_preco = preco + (preco*0.1)

    print(f'Aumentando 10%, temos R$ {aumento_preco}')

def desconto(preco):
    desconto_preco = preco - (preco*0.1)

    print(f'Descontando 10%, temos R$ {desconto_preco}')